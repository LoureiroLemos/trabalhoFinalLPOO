    private void btnIncluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIncluirActionPerformed
        // TODO add your handling code here:
        String nome = txtNome.getText();
        String sobrenome = txtSobrenome.getText();
        String rg = txtRg.getText();
        String cpf = txtCpf.getText();
        String endereco = txtEndereco.getText();
        

        
        if(!nome.isEmpty() && !cpf.isEmpty()){
            Cliente novo = new Cliente(nome, sobrenome, rg, cpf, endereco);
            clienteTableModel.adicionarCliente(novo);
            
            txtNome.setText("");
            txtSobrenome.setText("");
            txtRg.setText("");
            txtCpf.setText("");
            txtEndereco.setText("");
        }
    }//GEN-LAST:event_btnIncluirActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        int linhaSelecionada = tblClientes.getSelectedRow();
        if(linhaSelecionada != -1){
            clienteTableModel.removerCliente(linhaSelecionada);
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
        // TODO add your handling code here:
        int linhaSelecionada = tblClientes.getSelectedRow();
        if(linhaSelecionada !=-1){
            Cliente atualizado = new Cliente(txtNome.getText(),txtSobrenome.getText(),txtRg.getText(), txtCpf.getText(), txtEndereco.getText());
            clienteTableModel.atualizarCliente(linhaSelecionada, atualizado);
        }
    }//GEN-LAST:event_btnAtualizarActionPerformed

    private void tblClientesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblClientesMouseClicked
        // TODO add your handling code here:
        int linha = tblClientes.getSelectedRow();
        if (linha != -1){
            Cliente c = clienteTableModel.getClienteAt(linha);
            txtNome.setText(c.getNome());
            txtSobrenome.setText(c.getSobrenome());
            txtRg.setText(c.getRg());
            txtCpf.setText(c.getCpf());
            txtEndereco.setText(c.getEndereco());
        }
    }//GEN-LAST:event_tblClientesMouseClicked

